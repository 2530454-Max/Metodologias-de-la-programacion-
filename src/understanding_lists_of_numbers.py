# Metodo built-in range()
"""
    El metodo range nos ayuda a crear facilmente series de numeros:

    Ejemplo:
"""

print("Numeros impares del 1 al 9")
for value in range(1,10,2): # 10 numeros entre 0-9
        print(value)
print("Numeros pares del 1 al 9")
for value in range(2,10,2): #10 numeros entre 1-9
        print (value)

